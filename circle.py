# To find the circumference and area of a circle with a given radius

radius = float(input("Provide the radius = "))
Circumference = (2 * 22/7 * radius)
Area = (22/7 * radius * radius)

print("Circumference of a circle = {} Area of a circle = {}".format(Circumference, Area))
print(f"Circumference of a circle = {Circumference} Area of a circle = {Area}")
# Both ways can give you same solution)
